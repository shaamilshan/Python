shamilHobby = {"Design","Gaming","Playing"}
vishnuHobby = {"Football","Gaming","Gym"}

intersection = shamilHobby.intersection(vishnuHobby)
union = shamilHobby.union(vishnuHobby)
unique_hobbies = shamilHobby.symmetric_difference(vishnuHobby)

print("Intersection:", intersection)
print("Union:", union)
print("unique_hobbies:", unique_hobbies)